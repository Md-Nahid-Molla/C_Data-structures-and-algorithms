#include <stdio.h>
int main() {
  int x ;
  scanf("%d",&x);
  switch (x) {
      case 1:
      printf("valu is 1\n");
      break;
      case 2:
      printf("valu is 2\n");
      break;
      case 3:
      printf("valu is 3\n");
      break;
      default:
      printf("unknown valu\n");
  }
  return 0;
}
